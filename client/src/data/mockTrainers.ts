// Search functions
export const searchTrainerByName = (name: string): MockTrainer[] => {
  const nameParts = name.trim().split(/\s+/);
  
  // Handle single word search (could be last name)
  if (nameParts.length === 1) {
    const searchTerm = nameParts[0].toLowerCase();
    return mockTrainers.filter((trainer) => {
      const trainerFirstLower = trainer.firstName.toLowerCase();
      const trainerLastLower = trainer.lastName.toLowerCase();
      
      // Check if it matches last name or any part of first name
      return trainerLastLower === searchTerm || 
             trainerFirstLower.includes(searchTerm) ||
             trainerFirstLower.split(' ').some(part => part === searchTerm);
    });
  }

  // Handle multiple words
  if (nameParts.length < 2) {
    return [];
  }

  let firstName, lastName;
  
  if (nameParts.length === 2) {
    firstName = nameParts[0];
    lastName = nameParts[1];
  } else {
    // Handle middle names
    firstName = nameParts[0];
    lastName = nameParts[nameParts.length - 1];
  }

  const firstNameLower = firstName.toLowerCase();
  const lastNameLower = lastName.toLowerCase();
  
  const result = mockTrainers.find((trainer) => {
    const trainerFirstLower = trainer.firstName.toLowerCase();
    const trainerLastLower = trainer.lastName.toLowerCase();
    
    // Exact match
    if (trainerFirstLower === firstNameLower && trainerLastLower === lastNameLower) {
      return true;
    }
    
    // Handle middle name variations
    const searchFullName = `${firstNameLower} ${lastNameLower}`;
    
    // Check if last name matches and first name is contained
    if (trainerLastLower === lastNameLower && 
        (trainerFirstLower.includes(firstNameLower) || firstNameLower.includes(trainerFirstLower))) {
      return true;
    }
    
    // For "Kennard Isaiah Love" scenario
    if (searchFullName.includes(trainerFirstLower) && searchFullName.includes(trainerLastLower)) {
      return true;
    }
    
    return false;
  });
  
  return result ? [result] : [];
};

export const searchTrainerByCertificationId = (certificationId: string): MockTrainer | null => {
  return mockTrainers.find(trainer => trainer.certificationId === certificationId) || null;
};